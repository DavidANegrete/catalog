# Decorator function to keep views accisible by some only.
def logInDecorator(f):
    @wraps(f)
    def wrapper(*args, **kwds):
        if 'username' not in login_session:
            return redirect (url_for('showLogin', next = request.url))
        return f(*args, **kwds)
    return wrapper

